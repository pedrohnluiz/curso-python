"""
Tipo None

O tipo de dado None em Python representa o tipo sem tipo, ou poderia ser conhecido tambem como
tipo vazio, porem, falar que é um tipo sem tipo é mais apropriado

OBS: O tipo None é SEMPRE especificado com a primeira letra maiúscula.

Certo: None
Errado: none

Quando utilizamos?

 - podemos utilizar None quando queremos criar uma variavel e inicializá-la com um tipo sem tipo, antes
 de recebermos um valor final.


OBS: o tipo None em python é sempre considerado como falso
"""

numeros = None

print(numeros)
print(type(numeros))


numeros = 1.44, 1.34, 5.67

print(numeros)
print(type(numeros))
